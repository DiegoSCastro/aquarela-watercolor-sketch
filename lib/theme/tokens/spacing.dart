/// 8pt grid spacing scale + golden ratio at the top.
/// Whitespace composes the layout — like unpainted paper.
class Space {
  const Space._();

  /// 4px — inside icons, tight padding.
  static const double xs = 4;

  /// 8px — between tight elements.
  static const double sm = 8;

  /// 16px — default element spacing.
  static const double md = 16;

  /// 24px — section spacing.
  static const double lg = 24;

  /// 40px — screen margins.
  static const double xl = 40;

  /// 64px — hero spacing.
  static const double xxl = 64;

  /// 104px — splash breathing room.
  static const double xxxl = 104;
}
